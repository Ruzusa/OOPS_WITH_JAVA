// Q2: Program to calculate the perimeter of a square
public class Q2_PerimeterSquare {
    public static void main(String[] args) {
        int side = 8; // arbitrary value for the side length of square

        int perimeter = 4 * side;

        System.out.println("Side = " + side);
        System.out.println("Perimeter of Square = " + perimeter);
    }
}
