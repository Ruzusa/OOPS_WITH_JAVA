// Q5: Working with String length() and charAt()
public class Q5_StringHannah {
    public static void main(String[] args) {
        String hannah = "Did Hannah see bees? Hannah did.";

        // a. length of the string
        int length = hannah.length();

        // b. character at index 12
        char ch = hannah.charAt(12);

        System.out.println("String: " + hannah);
        System.out.println("Length of string = " + length);
        System.out.println("Character at index 12 = " + ch);
    }
}
